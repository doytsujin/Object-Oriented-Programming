package clinic;

import clinic.Doctor;

public class Person {
	
	private String nome;
	String cognome;
	private String SSN;
	Doctor doc;
	
	Person(String first, String last, String ssn2) {
		this.nome=first;
		this.cognome=last;
		this.SSN=ssn2;
		return;
	}

	public String getSSN(){
		return this.SSN;
	}

	public String getFirst() {
		return this.nome;
	}

	public String getLast() {
		return this.cognome;
	}

	public Doctor getDoctor() {
		return this.doc;
	}

}
